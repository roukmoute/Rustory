/// Append a new EMPTY node at the end of the display order. When `link_from`
/// is provided, the referenced option of the referenced node is atomically
/// linked to the new node IN THE SAME transaction — the "create and link"
/// gesture never leaves an intermediate half-state.
pub fn add_node(
    db: &mut DbHandle,
    story_id: &str,
    link_from: Option<(&str, usize)>,
) -> Result<StructureWriteOutputDto, AppError> {
    let story = story_id.to_string();
    let link_from = link_from.map(|(node_id, index)| (node_id.to_string(), index));
    apply_structure_mutation(db, story_id, move |structure, _tx| {
        let new_id = next_free_node_id(structure);
        structure.nodes.push(CanonicalNode {
            id: new_id.clone(),
            text: String::new(),
            label: String::new(),
            image_asset_id: None,
            audio_asset_id: None,
            options: Vec::new(),
        });
        if let Some((source_node_id, option_index)) = link_from {
            let source = structure
                .nodes
                .iter_mut()
                .find(|n| n.id == source_node_id)
                .ok_or_else(|| node_missing(&story, &source_node_id))?;
            let option = source
                .options
                .get_mut(option_index)
                .ok_or_else(|| option_index_invalid(&story, &source_node_id, option_index))?;
            option.target = Some(new_id);
        }
        Ok(())
    })
    .map(|(out, ())| out)
}

/// Media assets of a deleted node, captured in-transaction for the
/// post-commit refcounted file GC.
type RemovedAssets = Vec<(String, String)>;

/// Delete a node from the graph. REFUSES the start node (the entry point must
/// exist). The node's media assets are deleted (rows in-transaction, files
/// GC'd post-commit when unreferenced) and ITS recovery buffer entry is
/// purged CONDITIONALLY (`story_id` AND `node_id` — the single per-story
/// draft row may belong to ANOTHER node still being edited; an unconditional
/// purge would silently lose that buffer, NFR8). Options on OTHER nodes that
/// pointed at the deleted node KEEP their target and become `destination à
/// corriger` — visible and repairable, never silently unlinked.
pub fn delete_node(
    db: &mut DbHandle,
    app_data_dir: &Path,
    story_id: &str,
    node_id: &str,
) -> Result<StructureWriteOutputDto, AppError> {
    let story = story_id.to_string();
    let node = node_id.to_string();
    let (out, removed_assets) = apply_structure_mutation(db, story_id, move |structure, tx| {
        if structure.start_node_id == node {
            return Err(delete_start_refused(&story));
        }
        let idx = structure
            .nodes
            .iter()
            .position(|n| n.id == node)
            .ok_or_else(|| node_missing(&story, &node))?;
        let removed = structure.nodes.remove(idx);

        // Reclaim the node's asset rows inside the transaction; the files are
        // GC'd AFTER commit (refcounted by content hash), mirroring
        // `remove_node_media`.
        let mut removed_assets: RemovedAssets = Vec::new();
        for asset_id in removed
            .image_asset_id
            .iter()
            .chain(removed.audio_asset_id.iter())
        {
            let info: Option<(String, String)> = tx
                .query_row(
                    "SELECT content_hash, file_name FROM assets WHERE id = ?1 AND story_id = ?2",
                    rusqlite::params![asset_id, story],
                    |r| Ok((r.get(0)?, r.get(1)?)),
                )
                .optional()
                .map_err(|e| transport_error(&e, "read_assets", &story))?;
            tx.execute(
                "DELETE FROM assets WHERE id = ?1 AND story_id = ?2",
                rusqlite::params![asset_id, story],
            )
            .map_err(|e| transport_error(&e, "delete_assets", &story))?;
            if let Some(info) = info {
                removed_assets.push(info);
            }
        }

        // Purge the recovery buffer FOR THIS NODE ONLY. The table's PK is
        // `story_id` (one draft per story), so the row may buffer another
        // node's unsaved content — the WHERE node_id clause protects it.
        tx.execute(
            "DELETE FROM node_drafts WHERE story_id = ?1 AND node_id = ?2",
            rusqlite::params![story, node],
        )
        .map_err(|e| transport_error(&e, "delete_node_draft", &story))?;

        Ok(removed_assets)
    })?;

    // Post-commit, best-effort refcounted file GC (same helper as the media
    // removal path).
    let media_dir = crate::infrastructure::filesystem::resolve_node_media_dir(app_data_dir);
    for info in removed_assets {
        crate::application::story::node::gc_unreferenced_media_file(db, &media_dir, Some(info));
    }

    Ok(out)
}

/// Swap a node with its neighbor in the DISPLAY order. The start node is
/// designated by `startNodeId`, NOT by position 0 — moving it is legitimate.
/// A move with no room (already first / last) is an acknowledged no-op: the
/// graph is rewritten identical, never refused (a stale double-click must not
/// surface an error).
pub fn move_node(
    db: &mut DbHandle,
    story_id: &str,
    node_id: &str,
    direction: MoveDirection,
) -> Result<StructureWriteOutputDto, AppError> {
    let story = story_id.to_string();
    let node = node_id.to_string();
    apply_structure_mutation(db, story_id, move |structure, _tx| {
        let idx = structure
            .nodes
            .iter()
            .position(|n| n.id == node)
            .ok_or_else(|| node_missing(&story, &node))?;
        match direction {
            MoveDirection::Up if idx > 0 => structure.nodes.swap(idx, idx - 1),
            MoveDirection::Down if idx + 1 < structure.nodes.len() => {
                structure.nodes.swap(idx, idx + 1)
            }
            _ => {}
        }
        Ok(())
    })
    .map(|(out, ())| out)
}

/// Add an option `{label, target: None}` to a node. The label is typed at
/// creation and bounded by the same cap as the node metadata label.
pub fn add_option(
    db: &mut DbHandle,
    story_id: &str,
    node_id: &str,
    label: &str,
) -> Result<StructureWriteOutputDto, AppError> {
    if label.chars().count() > MAX_NODE_LABEL_CHARS {
        return Err(AppError::local_storage_unavailable(
            "Ajout impossible: libellé d'option trop long.",
            "Réduis la taille du libellé puis réessaie.",
        )
        .with_details(serde_json::json!({ "source": "option_label_too_long" })));
    }
    let story = story_id.to_string();
    let node = node_id.to_string();
    let label = label.to_string();
    apply_structure_mutation(db, story_id, move |structure, _tx| {
        let target_node = structure
            .nodes
            .iter_mut()
            .find(|n| n.id == node)
            .ok_or_else(|| node_missing(&story, &node))?;
        target_node.options.push(CanonicalOption {
            label,
            target: None,
        });
        Ok(())
    })
    .map(|(out, ())| out)
}

/// Set an option's destination. `Some(target)`: REFUSED unless the target
/// node exists in the graph — an invalid link is PREVENTED at write time
/// (AC2), never persisted; self-reference is a legitimate narrative loop and
/// is allowed. `None`: unlink (back to `non liée`).
pub fn set_option_link(
    db: &mut DbHandle,
    story_id: &str,
    node_id: &str,
    option_index: usize,
    target: Option<&str>,
) -> Result<StructureWriteOutputDto, AppError> {
    let story = story_id.to_string();
    let node = node_id.to_string();
    let target = target.map(str::to_string);
    apply_structure_mutation(db, story_id, move |structure, _tx| {
        if let Some(target_id) = target.as_deref() {
            let exists = structure.nodes.iter().any(|n| n.id == target_id);
            if !exists {
                return Err(link_target_missing(&story, target_id));
            }
        }
        let source = structure
            .nodes
            .iter_mut()
            .find(|n| n.id == node)
            .ok_or_else(|| node_missing(&story, &node))?;
        let option = source
            .options
            .get_mut(option_index)
            .ok_or_else(|| option_index_invalid(&story, &node, option_index))?;
        option.target = target;
        Ok(())
    })
    .map(|(out, ())| out)
}

/// Remove an option from a node (an out-of-range index is a typed refusal).
pub fn remove_option(
    db: &mut DbHandle,
    story_id: &str,
    node_id: &str,
    option_index: usize,
) -> Result<StructureWriteOutputDto, AppError> {
    let story = story_id.to_string();
    let node = node_id.to_string();
    apply_structure_mutation(db, story_id, move |structure, _tx| {
        let source = structure
            .nodes
            .iter_mut()
            .find(|n| n.id == node)
            .ok_or_else(|| node_missing(&story, &node))?;
        if option_index >= source.options.len() {
            return Err(option_index_invalid(&story, &node, option_index));
        }
        source.options.remove(option_index);
        Ok(())
    })
    .map(|(out, ())| out)
}

