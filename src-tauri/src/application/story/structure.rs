//! STRUCTURAL application services (schema v3): mutate the story's node
//! graph — add / delete / move a node, add / link / remove an option.
//!
//! Sibling of the node-content services (`node.rs`) with the SAME
//! transactional spine: `BEGIN IMMEDIATE`, the fail-closed `story_edit_scope`
//! guard, refusal on persisted BLOCKING facts, whole-graph mutation,
//! re-serialization + per-write checksum recompute, a post-mutation
//! `has_blocking` defense, one UPDATE, commit (NFR9 / FR18). Differences by
//! design: the mutation closure receives the WHOLE `CanonicalStructure` (not
//! one node), and the node draft is NEVER consumed here (the draft protects
//! CONTENT; the frontend flushes pending content BEFORE any structural
//! mutation — deleting a node purges ITS draft only, conditionally).
//!
//! A structural write may legitimately LEAVE a Fixable `BrokenOptionLink`
//! behind (deleting a referenced node keeps the pointing options, flagged
//! `destination à corriger`) — the post-mutation defense therefore rejects
//! BLOCKING blockers only, mirroring the node path.

