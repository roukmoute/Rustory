fn story_missing(story_id: &str) -> AppError {
    AppError::library_inconsistent(
        "Histoire introuvable, recharge la bibliothèque.",
        "Retourne à la bibliothèque et recharge la liste.",
    )
    .with_details(serde_json::json!({ "source": "story_missing", "id": story_id }))
}

fn structure_corrupt(story_id: &str) -> AppError {
    AppError::library_inconsistent(
        "La structure interne de l'histoire est illisible ou incohérente.",
        "Relance Rustory pour reconstruire la vue cohérente.",
    )
    .with_details(serde_json::json!({ "source": "structure_corrupt", "id": story_id }))
}

fn node_missing(story_id: &str, node_id: &str) -> AppError {
    AppError::library_inconsistent(
        "Le nœud à modifier est introuvable dans l'histoire.",
        "Recharge l'éditeur puis réessaie.",
    )
    .with_details(
        serde_json::json!({ "source": "node_missing", "id": story_id, "nodeId": node_id }),
    )
}

/// Refusal of a structural write on a story outside the FULL edit scope
/// (same cause as the node content guard — a device-pack story's structure
/// is carried by the pack; dedicated source marker for support triage).
fn structure_not_editable(story_id: &str) -> AppError {
    AppError::library_inconsistent(
        "Le contenu de cette histoire est porté par le pack copié depuis l'appareil et ne peut pas être modifié ici.",
        "Tu peux modifier le titre depuis l'éditeur ; le contenu du pack reste celui de l'appareil.",
    )
    .with_details(serde_json::json!({ "source": "structure_not_editable", "id": story_id }))
}

/// Deleting the start node is refused: the story must keep its entry point.
fn delete_start_refused(story_id: &str) -> AppError {
    AppError::library_inconsistent(
        "Le nœud de départ ne peut pas être supprimé.",
        "Choisis un autre nœud à supprimer.",
    )
    .with_details(serde_json::json!({ "source": "delete_start_refused", "id": story_id }))
}

/// Linking toward a node absent from the graph is PREVENTED (AC2): the
/// invalid link is never written.
fn link_target_missing(story_id: &str, target_id: &str) -> AppError {
    AppError::library_inconsistent(
        "La destination choisie n'existe plus dans l'histoire.",
        "Recharge l'éditeur puis choisis un nœud existant.",
    )
    .with_details(serde_json::json!({
        "source": "link_target_missing",
        "id": story_id,
        "targetId": target_id,
    }))
}

/// The referenced option index does not exist on the node (stale UI state).
fn option_index_invalid(story_id: &str, node_id: &str, option_index: usize) -> AppError {
    AppError::library_inconsistent(
        "L'option à modifier est introuvable dans ce nœud.",
        "Recharge l'éditeur puis réessaie.",
    )
    .with_details(serde_json::json!({
        "source": "option_index_invalid",
        "id": story_id,
        "nodeId": node_id,
        "optionIndex": option_index,
    }))
}

fn transport_error(_err: &rusqlite::Error, stage: &'static str, story_id: &str) -> AppError {
    AppError::local_storage_unavailable(
        "Rustory n'a pas pu enregistrer ta modification.",
        "Réessaie dans un instant ; si le problème persiste, consulte les traces locales.",
    )
    .with_details(serde_json::json!({
        "source": "sqlite_structure_write",
        "stage": stage,
        "id": story_id,
    }))
}

/// `true` when the canonical facts carry at least one BLOCKING blocker. A
/// fixable-only set (an invalid persisted title, a broken option link) never
/// blocks a structural write — repairing IS a structural write.
fn has_blocking(facts: &CanonicalStoryFacts) -> bool {
    validate_canonical(facts)
        .iter()
        .any(|b| b.severity == Severity::Blocking)
}

