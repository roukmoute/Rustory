/// Project the node graph into its LIGHT wire DTO for the navigator. Pure —
/// no DB, no filesystem. Derives, in Rust only, each option's link `state`
/// (`unlinked` / `linked` / `broken`) and each node's localized `has_issue`
/// flag (at least one broken option link).
pub fn project_structure(structure: &CanonicalStructure) -> StoryStructureDto {
    let ids: HashSet<&str> = structure.nodes.iter().map(|n| n.id.as_str()).collect();
    StoryStructureDto {
        start_node_id: structure.start_node_id.clone(),
        nodes: structure
            .nodes
            .iter()
            .map(|node| {
                let options: Vec<OptionLinkDto> = node
                    .options
                    .iter()
                    .map(|option| OptionLinkDto {
                        label: option.label.clone(),
                        target: option.target.clone(),
                        state: match option.target.as_deref() {
                            None => "unlinked",
                            Some(target) if ids.contains(target) => "linked",
                            Some(_) => "broken",
                        }
                        .to_string(),
                    })
                    .collect();
                NodeGraphDto {
                    id: node.id.clone(),
                    label: node.label.clone(),
                    is_start: node.id == structure.start_node_id,
                    has_issue: options.iter().any(|o| o.state == "broken"),
                    options,
                }
            })
            .collect(),
    }
}

/// The smallest free `n<k>` id (n1, n2, …) — deterministic and generated by
/// Rust only, never by the frontend. Uniqueness is then re-guarded by the
/// graph validation (`DuplicateNodeId`).
///
/// "Free" reserves BOTH the existing node ids AND every `option.target` —
/// including targets whose node was deleted (a `destination à corriger`
/// trace). Recycling such an id would silently transmute a flagged broken
/// link into a live link toward an unrelated new node.
fn next_free_node_id(structure: &CanonicalStructure) -> String {
    let reserved: HashSet<&str> = structure
        .nodes
        .iter()
        .map(|n| n.id.as_str())
        .chain(
            structure
                .nodes
                .iter()
                .flat_map(|n| n.options.iter())
                .filter_map(|o| o.target.as_deref()),
        )
        .collect();
    let mut k: usize = 1;
    loop {
        let candidate = format!("n{k}");
        if !reserved.contains(candidate.as_str()) {
            return candidate;
        }
        k += 1;
    }
}

